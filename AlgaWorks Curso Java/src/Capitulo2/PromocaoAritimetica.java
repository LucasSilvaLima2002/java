package Capitulo2;

public class PromocaoAritimetica {
	public static void main(String[] args) {
		
		int x = 10;
		long y = 5;
		long z = x + y;
		
		System.out.println(z);
		
		
		float x1 = 9.31f;
		long y1 = 10;
		float z1 = x1*y1;
		
		System.out.println(z1);
		
		int x2 = 3;
		//se fosse int o resultado seria 1
		int y2 = 2;
		float z2 = (float)x2/y2;
		
		System.out.println(z2);
		
		
		
	}

}
