package fiap.com.br;

public class Operacoes {
	
public static String contador(int i, int f) {
	String s = "";
	
	for(int c = i; c<=f; c++) {
		s += c;
	}
	return s;
 }
public static int contador2 (int x, int h) {
	
	int p = 0;
	
	for(int m = x; m<=h; m++) {
		p += m;
		//a string concatena
	}
	return p;
	
}
}

