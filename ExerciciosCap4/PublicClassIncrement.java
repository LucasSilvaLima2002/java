public class PublicClassIncrement {
    public static void main(String[] args) {
        
        int c = 5;
        System.out.println("c antes do incremento " + c);
        System.out.println("pos incremento "+ c++);
        System.out.println("incrementado " + c);

        c = 5;
        System.out.println("c antes do incremento " + c);
        System.out.println("pos incremento "+ ++c);
        System.out.println("incrementado " + c);





    }
}
